export function getSpeciesBySlug(slug: string): FishSpecies | undefined {
  return fishSpecies.find((s) => s.slug === slug);
}

export function getRelatedSpecies(species: FishSpecies, limit = 4): FishSpecies[] {
  return fishSpecies
    .filter((s) => s.family === species.family && s.slug !== species.slug)
    .slice(0, limit);
}

export function getSpeciesByHabitat(category: HabitatCategory): FishSpecies[] {
  return fishSpecies.filter((s) => s.habitatCategory === category);
}

export function getSpeciesByCoast(coast: 'Persian Gulf' | 'Gulf of Oman' | 'Both'): FishSpecies[] {
  if (coast === 'Both') return fishSpecies;
  return fishSpecies.filter((s) => s.coast === coast || s.coast === 'Both');
}
