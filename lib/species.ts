export const fishSpecies: FishSpecies[] = rawSpecies.map((s) => ({
  ...s,
  slug: s.id,
  habitatCategory: getHabitatCategory(s.habitat),
  edibility: getEdibility(s.edible, s.dangerLevel, s.name),
}));

export function getSpeciesBySlug(slug: string): FishSpecies | undefined {
  return fishSpecies.find((s) => s.slug === slug);
}

export function getRelatedSpecies(species: FishSpecies, limit = 4): FishSpecies[] {
  return fishSpecies
    .filter((s) => s.slug !== species.slug && s.family === species.family)
    .slice(0, limit);
}

