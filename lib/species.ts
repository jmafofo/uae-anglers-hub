export type ConservationStatus =
  | 'Least Concern'
  | 'Near Threatened'
  | 'Vulnerable'
  | 'Endangered'
  | 'Critically Endangered'
  | 'Data Deficient'
  | 'Not Evaluated';

export type DangerLevel = 'none' | 'venomous' | 'potentially dangerous' | 'toxic' | 'dangerous';

export type Edibility = 'Excellent' | 'Good' | 'Fair' | 'Avoid' | 'Not Edible';

export type HabitatCategory = 'Reef' | 'Pelagic' | 'Demersal' | 'Coastal' | 'Open Ocean' | 'Freshwater' | 'Other';

